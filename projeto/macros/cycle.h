    #define FORI(n)             for(int i = 0; i < n; i++)
    #define FROMI_TO_N(x, n)    for(int i = x; i < n; i++)
    #define FORJ(n)             for(int j = 0; j < n; j++)
    #define FROMJ_TO_N(x, n)    for(int j = x; j < n; j++)
    #define REVERSE_FORI(n)     for(int i = n - 1; i >= 0; i--)

